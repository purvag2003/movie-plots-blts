class Movie:

    def __init__(self, id, release_year, title, origin, director, cast, genre, wiki, plot):
        self.id = id
        self.release_year = release_year
        self.title = title
        self.origin = origin
        self.director = director
        self.cast = cast
        self.genre = genre
        self.wiki = wiki
        self.plot = plot
